/*
 * Look up glyph for a Unicode codepoint.
 * ASCII: use the main font table.
 * Block/box-drawing: use the unicode table.
 * Everything else: tofu.
 */
static const unsigned char *
font8x16_glyph(uint32_t cp)
{
	if (cp >= 0x20 && cp <= 0x7E)
		return font8x16[cp - 0x20];
	for (unsigned i = 0; i < FONT_UNICODE_COUNT; i++) {
		if (font_unicode[i].cp == cp)
			return font_unicode[i].data;
	}
	if (cp == ' ' || cp == 0)
		return font8x16[0]; /* space */
	return font_tofu;
}

